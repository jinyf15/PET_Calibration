function [A,b,Aeq,beq,lb,ub]= constrains(x,fitIndex)

A=[];
b=[];
Aeq=[];
beq=[];
lb = zeros(1,size(x,2));

lb(1) = x(1) - 5;
ub(1) = x(1) + 5;
lb(42:43)=x(42:43) - 5;
ub(42:43)=x(42:43) + 5;
for i = 0:3
    lb(2+i*10:4+i*10) = x(2+i*10:4+i*10) - 10;
    ub(2+i*10:4+i*10) = x(2+i*10:4+i*10) + 10;
    lb(5+i*10:7+i*10) = x(5+i*10:7+i*10) - 5/180*pi;
    ub(5+i*10:7+i*10) = x(5+i*10:7+i*10) + 5/180*pi;
    lb(8+i*10:11+i*10) = x(8+i*10:11+i*10) - 0.5;
    ub(8+i*10:11+i*10) = x(8+i*10:11+i*10) + 0.5;
end
for i = 0:1
    lb(3+i*10) = x(3+i*10) - 20;
    ub(3+i*10) = x(3+i*10) + 20;
    lb(22+i*10) = x(22+i*10) - 20;
    ub(22+i*10) = x(22+i*10) + 20;
end
lb(fitIndex==0) = [];
ub(fitIndex==0) = [];